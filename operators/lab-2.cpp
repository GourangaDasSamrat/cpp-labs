// All assignment operators in C++

#include <iostream>
using namespace std;

int main() {
  // Arithmetic assignment operators
  int a = 10;
  a += 5;  // +=

  int b = 10;
  b -= 5;  // -=

  int c = 10;
  c *= 5;  // *=

  int d = 10;
  d /= 5;  // /=

  int e = 10;
  e %= 3;  // %=

  // prints
  cout << a << endl;
  cout << b << endl;
  cout << c << endl;
  cout << d << endl;
  cout << e << endl;

  return 0;
}
